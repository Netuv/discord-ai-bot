// ─── Tests: validateArticleForDiscord ─────────────────────

describe("validateArticleForDiscord", () => {
  it("should return ok for valid article", () => {
    const article = makeArticle();
    const result = validateArticleForDiscord(article);
    expect(result.ok).toBe(true);
  });

  it("should return not ok for empty sections", () => {
    const article = makeArticle({ sections: [] });
    const result = validateArticleForDiscord(article);
    expect(result.ok).toBe(false);
    expect(result.issues.length).toBeGreaterThan(0);
  });

  it("should return issues array for invalid article", () => {
    const article = makeArticle({ sections: [] });
    const result = validateArticleForDiscord(article);
    expect(result.issues.some(i => i.includes("error") || i.includes("🛑"))).toBe(true);
  });
});

// ─── Tests: EYD Check ────────────────────────────────────

describe("EYD Check", () => {
  it("should warn about missing space after period", () => {
    const article = makeArticle();
    article.intro = "Anime ini bagus.Sekarang kita bahas.";
    const report = auditArticle(article);
    // Should have EYD warning about space after period
    expect(report.issues.some(i => i.message.includes("Spasi setelah tanda baca"))).toBe(true);
  });

  it("should warn about lowercase after period", () => {
    const article = makeArticle();
    article.intro = "Anime ini bagus. sekarang kita bahas.";
    const report = auditArticle(article);
    expect(report.issues.some(i => i.message.includes("Huruf kecil setelah titik"))).toBe(true);
  });

  it("should warn about excessive spacing", () => {
    const article = makeArticle();
    article.intro = "Anime   ini   bagus";
    const report = auditArticle(article);
    expect(report.issues.some(i => i.message.includes("Spasi berlebih"))).toBe(true);
  });
});
